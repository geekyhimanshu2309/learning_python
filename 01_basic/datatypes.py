# Datatypes / Object types in Python
# Python is a dynamically typed language, which means that you don't have to declare the type of a variable when you create one. Python automatically assigns the most appropriate type to a variable based on the value it is assigned.
# - Number: 1234, 3.145, 3+4j, 0b111, Decimal, Fraction
# - String: 'spam', "guido's", b'a\x01c', u'sp\xc4m'
# - List: [1, [2, 'three'], 4], list(range(10))
# - Tuple: (1, 'spam', 4, 'U'), tuple('spam'), namedtuple
# - Set: set('abc'), {'a', 'b', 'c'}
# - Dictionary: {'food': 'spam', 'taste': 'yum'}, dict(hours=10)
# - Function: def foo(): pass
# - Type: type(42), type(3.14), type('spam')
# - Class: class Foo: pass
# - Instance: Foo()
# - Method: Foo().method()
# - Module: import sys
# - Files: open('eggs.txt')
# - Boolean: True, False
# - None: None
# - Bytes: b'spam'
# - Bytearray: bytearray(b'spam')
# - Memoryview: memoryview(b'abc')
# - Range: range(10)
# - Frozenset: frozenset('abc')
